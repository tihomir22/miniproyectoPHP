			<div class="col-sm-4 col-md-8 "><p class="text-justify">Fue noticia en todos los telediarios del pasado 28 de mayo. Un niño de tres años cayó al foso de uno de los animales del zoo de Cincinnati (Ohio, Estados Unidos). Se trataba del área reservada a un gorila de lomo plateado, un animal de 180 kilos y 17 años de edad. Su nombre era Harambe, como la canción de Rita Marley. La seguridad del zoo abatió al animal para salvar al niño, una decisión que causó una controversia que aún hoy no se ha resuelto.<br>

				Los expertos en primates no se aclaran. Algunos respaldan la decisión del zoológico estadounidense, como la reputada Jane Goodall, sin olvidar que el primate mostraba una actitud “protectora”. El primatólgo Frans de Waal recordaba en El País la dificultad que entrañaba la decisión, al igual que el presidente de la Asociación Ibérica de Zoos y Acuarios, Jesús Fernández.
				Mientras tanto, la opinión de una gran parte de los usuarios de Internet es cristalina: abatirle fue un error. Por ello, la web está plagada de memes, iniciativas, vídeos y textos en memoria del primate. La ironía que inunda la red no sienta bien a “los más afligidos” por la pérdida del simio, el propio zoo de Cincinnati: “La familia del zoológico aún se está recuperando. Las constantes menciones a Harambe hacen que superarlo sea más difícil”.<br>

				Así se expresa el director del recinto, Thane Maynard, entrevistado por Asociated Press. “No disfrutamos de los memes, las peticiones y las recogidas de firmas sobre Harambe”, añade Maynard, que asegura estar “honrando” al gorila “redoblando los esfuerzos para las conservación de los gorilas; animamos a otros a hacer lo mismo”. La presencia de Harambe en la web es constante desde que fue abatido.

				En Change.org, una campaña pide “justicia para Harambe”, incluso entrando a valorar la situación familiar del niño que protagonizó el incidente. Acumula más de medio millón de firmas. Otra petición en la misma página web reclama a Nintendo un pokémon en honor al gorila. El creador de la campaña, que suma casi 100.000 firmas, ha diseñado cómo le imagina e incluso ha imaginado un pokémon que evolucionaría en Harambe<br>
